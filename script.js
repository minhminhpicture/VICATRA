const GOOGLE_SHEETS_WEB_APP_URL =
  "https://script.google.com/macros/s/AKfycbzXg-x8UghSdUSHZ4SFCyJq0kXvVLzZG5bqO_5wdwhZyUQPXEximUIF02oo20cPGTLj/exec";
const form = document.querySelector("#leadForm");
const note = document.querySelector("#formNote");
const submitButton = form?.querySelector("button[type='submit']");
const languageButtons = document.querySelectorAll("[data-lang]");
const revealItems = document.querySelectorAll(".reveal");
let currentLanguage = localStorage.getItem("vicatra-language") || "vi";

function applyLanguage(language) {
  currentLanguage = language;
  localStorage.setItem("vicatra-language", language);
  document.documentElement.lang = language;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = translations[language][key] || translations.vi[key] || "";
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const key = element.dataset.i18nHtml;
    element.innerHTML = translations[language][key] || translations.vi[key] || "";
  });

  document.querySelectorAll("[data-placeholder]").forEach((element) => {
    const key = element.dataset.placeholder;
    element.setAttribute("placeholder", translations[language][key] || translations.vi[key] || "");
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === language);
  });
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

form?.addEventListener("submit", async (event) => {
  event.preventDefault();
  const formData = new FormData(form);
  const fallbackName = currentLanguage === "vi" ? "Quý khách" : "Thank you";
  const name = formData.get("name")?.toString().trim() || fallbackName;

  if (!GOOGLE_SHEETS_WEB_APP_URL) {
    note.textContent = translations[currentLanguage].formMissingEndpoint;
    return;
  }

  // Chuyển FormData sang URLSearchParams để Google Apps Script nhận diện e.parameter tốt hơn
  const params = new URLSearchParams();
  for (const [key, value] of formData.entries()) {
    params.append(key, value);
  }
  params.append("language", currentLanguage);
  params.append("page", window.location.href);

  note.textContent = translations[currentLanguage].formSending;
  submitButton.disabled = true;

  try {
    await fetch(GOOGLE_SHEETS_WEB_APP_URL, {
      method: "POST",
      mode: "no-cors",
      headers: {
        "Content-Type": "application/x-www-form-urlencoded",
      },
      body: params.toString(),
    });

    note.textContent = translations[currentLanguage].formSuccess.replace("{name}", name);
    form.reset();
  } catch (error) {
    note.textContent = translations[currentLanguage].formError;
  } finally {
    submitButton.disabled = false;
  }
});

if ("IntersectionObserver" in window) {
  const revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          revealObserver.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.15 }
  );

  revealItems.forEach((item) => revealObserver.observe(item));
} else {
  revealItems.forEach((item) => item.classList.add("is-visible"));
}

applyLanguage(currentLanguage);
